package com.ruiyang.du.bo;

public class ModifyAppidVO {
    private String wechatMerchantNo;
    private String appid;
    private String focusAppid;

    public String getWechatMerchantNo() {
        return wechatMerchantNo;
    }

    public void setWechatMerchantNo(String wechatMerchantNo) {
        this.wechatMerchantNo = wechatMerchantNo;
    }

    public String getAppid() {
        return appid;
    }

    public void setAppid(String appid) {
        this.appid = appid;
    }

    public String getFocusAppid() {
        return focusAppid;
    }

    public void setFocusAppid(String focusAppid) {
        this.focusAppid = focusAppid;
    }
}
